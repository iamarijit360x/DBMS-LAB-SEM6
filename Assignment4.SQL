DROP TABLE EMPLOYEES;
DROP TABLE CUSTOMERS;
DROP TABLE ZIPCODES;

DROP TABLE PARTS;
DROP TABLE ORDERS;
DROP TABLE ODETAILS;

CREATE TABLE ZIPCODES(ZIP NUMBER(6) PRIMARY KEY,
CITY VARCHAR2(20));


CREATE TABLE EMPLOYEES(ENO VARCHAR2(6) PRIMARY KEY,
ENAME VARCHAR2(20),
ZIP NUMBER(6) REFERENCES ZIPCODES(ZIP),
HDATE DATE);

CREATE TABLE PARTS(PNO VARCHAR2(6) PRIMARY KEY,
PNAME VARCHAR2(20),
QOH NUMBER(3),
PRICE NUMBER(5),
LVL NUMBER(2)
);

CREATE TABLE CUSTOMERS(CNO VARCHAR2(6) PRIMARY KEY,
CNAME VARCHAR2(20),
STREET VARCHAR2(20),
ZIP NUMBER(6) REFERENCES ZIPCODES(ZIP),
PHONE NUMBER(10)
);


CREATE TABLE ORDERS(ONO VARCHAR2(6) PRIMARY KEY,
CNO VARCHAR2(6),
ENO VARCHAR2(6),
RECIECED DATE,
SHIPPED DATE);

CREATE TABLE ODETAILS(ONO VARCHAR2(6) REFERENCES ORDERS(ONO),
PNO VARCHAR2(6) REFERENCES PARTS(PNO),
QTY NUMBER(6),
PRIMARY KEY(ONO,PNO));


